package de.teamwaldstadt.picsimu.command;

public enum CommandNames {
	
	//schema: 
	//name (hexCode of command, mask for command, class name)
	ADDWF (0x0700, 0xFF00),
	ANDWF (0x0500, 0xFF00),
	CLRF (0x0180, 0xFF80),
	CLRW (0x0100, 0xFF80),
	COMF (0x0900, 0xFF00),
	DECF (0x0300, 0xFF00),
	DECFSZ (0x0B00, 0xFF00),
	INCF (0x0A00, 0xFF00),
	INCFSZ (0x0F00, 0xFF00),
	IORWF (0x0400, 0xFF00),
	MOVF (0x0800, 0xFF00),
	MOVWF (0x0080, 0xFF80),
	NOP (0x0000, 0xFF9F),
	RLF (0x0D00, 0xFF00),
	RRF (0x0C00, 0xFF00),
	SUBWF (0x0200, 0xFF00),
	SWAPF (0x0E00, 0xFF00),
	XORWF (0x0600, 0xFF00),
	
	BCF (0x1000, 0xFC00),
	BSF (0x1400, 0xFC00),
	BTFSC (0x1800, 0xFC00),
	BTFSS (0x1C00, 0xFC00),
	
	ADDLW (0x3E00, 0xFE00),
	ANDLW (0x3900, 0xFF00),
	CALL (0x2000, 0xF800),
	CLRWDT (0x0064, 0xFFFF),
	GOTO (0x2800, 0xF800),
	IORLW (0x3800, 0xFF00),
	MOVLW (0x3000, 0xFC00),
	RETFIE (0x0009, 0xFFFF),
	RETLW (0x3400, 0xFC00),
	RETURN (0x0008, 0xFFFF),
	SLEEP (0x0063, 0xFFFF),
	SUBLW (0x3C00, 0xFE00),
	XORLW (0x3A00, 0xFF00);

	
	private int value;
	private int mask;
	
	private CommandNames(int value, int mask) {
		this.value = value;
		this.mask = mask;
	}
	
	public int getValue() {
		return this.value;
	}
	
	public int getMask() {
		return this.mask;
	}
}
